    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        selectedIndex = tbNV.getSelectedRow();
        JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa ?");
        Nhanvien nv = nhanvien.get(selectedIndex);
        try {
            new DAOnv().DeleteNV(nv.getId());
            showtbNV();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        selectedIndex = tbNV.getSelectedRow();
        Nhanvien n = nhanvien.get(selectedIndex);
        try {
            new DAOnv().DeleteNV(n.getId());
        } catch (Exception e) {
            e.printStackTrace();
        }
        if (nhanvien.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không có dữ liệu để sữa");
        } else if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn một dòng mà sữa");
        } else {
            Nhanvien nv = new Nhanvien();
            nv.setManv(txtMaNV.getText());
            nv.setHoten(txtTen.getText());
            String sex = "";
            if (rdonam.isSelected()) {
                sex += "nam";
            } else {
                sex += "Nữ";
            }
            nv.setChucvu(cmdcv.getSelectedItem().toString());
            nv.setNgaysinh(txtngaysinh.getText());
            nv.setDiachi(txtaddress.getText());
            nv.setSDT(txtsdt.getText());
            nv.setEmail(txtemail.getText());
            nv.setSex(sex);
            boolean isOK = true;
            try {
                nv.setLuong(Integer.parseInt(txtluong.getText()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Là số không phải là kí tự");
                isOK = false;
            }
            if (isOK) {
                try {
                    new DAOnv().AddNV(nv);
                    showtbNV();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
        JOptionPane.showMessageDialog(this, "Sữa thành công");
    }//GEN-LAST:event_btnupdateActionPerformed

    private void tbNVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbNVMouseClicked
        selectedIndex = tbNV.getSelectedRow();
        Nhanvien nv = nhanvien.get(selectedIndex);
        txtMaNV.setText(nv.getManv());
        txtTen.setText(nv.getHoten());
        String sex = nv.getSex();
        if (sex.equals("Nam")) {
            rdonam.setSelected(true);
        } else {
            rdonu.setSelected(true);
        }
        cmdcv.setSelectedItem(nv.getChucvu());
        txtngaysinh.setText(nv.getNgaysinh());
        txtaddress.setText(nv.getDiachi());
        txtsdt.setText(nv.getSDT());
        txtemail.setText(nv.getEmail());
        txtluong.setText(nv.getLuong() + "");
    }//GEN-LAST:event_tbNVMouseClicked

    private void btnfindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfindActionPerformed
        String Name = txtTen.getText();
        if (Name.length() > 0) {
            try {
                nhanvien = new DAOnv().findbyNhanvien(Name);
            } catch (Exception e) {
                e.printStackTrace();
            }
            Model.setRowCount(0);
            for (Nhanvien nv : nhanvien) {
                Model.addRow(new Object[]{
                    tbNV.getRowCount() + 1, nv.getManv(), nv.getHoten(), nv.getSex(), nv.getChucvu(), nv.getNgaysinh(), nv.getDiachi(), nv.getSDT(), nv.getEmail(), nv.getLuong()
                });
            }
        }
    }//GEN-LAST:event_btnfindActionPerformed

