    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        if (txtMaNV.getText().equals("") || txtTen.getText().equals("") || txtngaysinh.getText().equals("") || txtaddress.getText().equals("") || txtsdt.getText().equals("") || txtemail.getText().equals("")) {
            JOptionPane.showConfirmDialog(this, "Nhập đầy đầy đủ thông tin", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            Nhanvien nv = new Nhanvien();
            nv.setManv(txtMaNV.getText());
            nv.setHoten(txtTen.getText());
            String sex = "";
            if (rdonam.isSelected()) {
                sex += "nam";
            } else {
                sex += "Nữ";
            }
            nv.setChucvu(cmdcv.getSelectedItem().toString());
            nv.setNgaysinh(txtngaysinh.getText());
            nv.setDiachi(txtaddress.getText());
            nv.setSDT(txtsdt.getText());
            nv.setEmail(txtemail.getText());
            nv.setSex(sex);
            boolean isOK = true;
            try {
                nv.setLuong(Integer.parseInt(txtluong.getText()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Là số không phải là kí tự");
                isOK = false;
            }
            if (isOK) {
                try {
                    new DAOnv().AddNV(nv);
                    showtbNV();
                } catch (Exception e) {
                    e.printStackTrace();
                }
                JOptionPane.showMessageDialog(this, "Thêm thành công");
            }
        }
    }//GEN-LAST:event_btnaddActionPerformed

