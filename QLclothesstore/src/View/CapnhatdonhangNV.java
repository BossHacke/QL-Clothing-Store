    private void btnaddhoadonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddhoadonActionPerformed
        if (txtMaHD.getText().equals("") || txtDateoder.getText().equals("") || txtThanhtien.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "nhập đầy đủ thông tin", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            Hoadon hd = new Hoadon();
            hd.setMaHD(Integer.parseInt(txtMaHD.getText()));
            hd.setKhachHang(txtkh.getText());
            hd.setTensp(txttensp.getText());
            hd.setGia(Integer.parseInt(txtgia.getText()));
            hd.setSize(txtsize.getText());
            hd.setDateoder(txtDateoder.getText());
            hd.setSoluong(Integer.parseInt(txtSl.getText()));
            hd.setThanhtien(Integer.parseInt(txtThanhtien.getText()));
            try {
                new DAOhd().AddHD(hd);
                showtbhoadon();
            } catch (Exception e) {
                e.printStackTrace();
            }
            JOptionPane.showMessageDialog(this, "Thêm hóa đơn thành công");
        }
    }//GEN-LAST:event_btnaddhoadonActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        selectedIndex = tbhoadon.getSelectedRow();
        JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa");
        Hoadon hd = hoadon.get(selectedIndex);
        try {
            new DAOhd().DeleteSP(hd.getMaHD());
            showtbhoadon();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btndeleteActionPerformed

