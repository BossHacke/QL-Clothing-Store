    private void showtbhoadon() throws ClassNotFoundException, SQLException {
        hoadon = new DAOhd().getListHD();
        ModelHD.setRowCount(0);
        for (Hoadon hd : hoadon) {
            ModelHD.addRow(new Object[]{
                hd.getMaHD(), hd.getKhachHang(), hd.getTensp(), hd.getGia(), hd.getSize(), hd.getDateoder(), hd.getSoluong(), hd.getThanhtien()
            });
        }
    }
    
    private void showtbsanpham() throws ClassNotFoundException, SQLException {
        sanpham = new DAOsp().getListSP();
        ModelSP.setRowCount(0);
        for (Sanpham sp : sanpham) {
            ModelSP.addRow(new Object[]{
                tbsp.getRowCount() + 1, sp.getMasp(), sp.getTensp(), sp.getLoai(), sp.getSize(), sp.getSoluong(), sp.getGia()
            });
        }
    }
    
    private void showtbSanphamCT() throws ClassNotFoundException, SQLException {
        sanphamCT = new DAOspCT().getListSanphamCT();
        ModelSPCT.setRowCount(0);
        for (SanphamCT sp : sanphamCT) {
            ModelSPCT.addRow(new Object[]{
                tbSanphamCT.getRowCount() + 1, sp.getMaspID(), sp.getSoLuong(), sp.getGia()
            });
        }
    }
